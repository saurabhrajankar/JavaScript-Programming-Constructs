let num=100;

switch(num)
{
    case 1:
        console.log("Units Place");
        break;
    case 10:
        console.log("Tens Place");
        break;
    case 100:
        console.log("Hundreds Place");
        break;
    case 1000:
        console.log("Thousands Place");
        break;
    case 100000:
        console.log("Lakhs Place");
        break;
}